#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*strs;

	strs = (char *)s;
	i = 0;
	while (i < n)
	{
		strs[i] = 0;
		i++;
	}
	return (s);
}
